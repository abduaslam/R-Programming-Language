#Operators 
#In R, there are four main types of operators:
#Assignment 
#Arithmetic
#Relational 
#Logical


#Assignment operators assign values to variables. 
#In R, the best practice is to use the arrow assignment (<-)
#example
x<-20
names<-c("abdu","omer","john")

#Arithmetic operators perform basic math operations,
#such as addition, subtraction, multiplication, and division. 
#Examples
x <- 2
y <- 5
x+y
x-y
x*y
x/y
x^y
x%%y
x%/%y

#Relational operators, also known as comparators, allow you to compare values.
#The output for relational operators is either TRUE or FALSE
x=20
y=10
x>y
x>=y
x<y
x<=y
x!=y
x==y

#Logical operators allow you to combine logical statements and return a logical value, TRUE or FALSE
#Exampls
y <- 7
y < 8 | y > 16
y<8 & y>16



